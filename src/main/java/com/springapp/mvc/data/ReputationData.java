public class ReputationData {
    String productId;
    String mallName;
    String repuData;
    String repuUrl;

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getMallName() {
        return mallName;
    }

    public void setMallName(String mallName) {
        this.mallName = mallName;
    }

    public String getRepuData() {
        return repuData;
    }

    public void setRepuData(String repuData) {
        this.repuData = repuData;
    }

    public String getRepuUrl() {
        return repuUrl;
    }

    public void setRepuUrl(String repuUrl) {
        this.repuUrl = repuUrl;
    }
}
